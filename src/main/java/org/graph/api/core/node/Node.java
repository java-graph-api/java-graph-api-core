package org.graph.api.core.node;

import org.graph.api.core.GraphState;

import java.util.UUID;

/**
 * Представляет собой узел в конвейере выполнения графа. Реализации принимают входные данные типа
 * {@code I}, работают с изменяемым состоянием {@link GraphState} типа {@code S} и возвращают
 * результат типа {@code O}.
 * <p>
 * Узлы имеют уникальный идентификатор и содержат описательные метаданные, которые могут использоваться
 * сборщиками графов и инструментами для понимания их возможностей и ограничений.
 *
 * @param <I> тип входных данных, обрабатываемых узлом
 * @param <O> тип выходных данных, производимых узлом
 * @param <S> тип состояния, доступного между вызовами узлов
 */
public interface Node<I, O, S extends GraphState> {

    /**
     * Человекочитаемое имя узла, обычно используемое для отладки, логирования или визуализации.
     *
     * @return имя узла
     */
    String getName();

    /**
     * Выполняет логику узла, используя переданные входные данные и общее состояние графа.
     *
     * @param input входное значение, передаваемое узлу
     * @param state изменяемое состояние графа, доступное во время выполнения
     * @return выходные данные, сформированные узлом
     */
    O call(I input, S state);

    /**
     * Описательные метаданные узла, которые могут использоваться фреймворками или инструментами для
     * конфигурации, документирования или валидации.
     *
     * @return метаданные, описывающие узел
     */
    Metadata getMetadata();

    /**
     * Уникальный идентификатор экземпляра узла. Реализация должна возвращать стабильные идентификаторы,
     * чтобы на узлы можно было ссылаться при построении и выполнении графа.
     *
     * @return UUID узла
     */
    UUID getId();

    /**
     * Необязательное ограничение, показывающее, сколько раз узел может быть вызван при выполнении графа.
     * Значение {@code 0} означает отсутствие ограничения.
     *
     * @return максимальное число вызовов или {@code 0} при отсутствии ограничения
     */
    default int callLimit() {
        return 0;
    }
}

